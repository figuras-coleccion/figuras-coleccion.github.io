function paintLogo(canvas) {
  if (!(canvas instanceof HTMLCanvasElement)) return
  canvas.width = 48
  canvas.height = 48
  canvas.setAttribute('aria-label', 'Panini Collection 2026')
  canvas.setAttribute('role', 'img')
  const context = canvas.getContext('2d', { alpha: false })
  if (!context) return

  ROWS.forEach((encodedRow, y) => {
    let x = 0
    encodedRow.split(',').forEach(run => {
      const [countText, colorText] = run.split(':')
      const count = Number(countText)
      const colorIndex = Number(colorText)
      if (!Number.isFinite(count) || !Number.isFinite(colorIndex)) return
      context.fillStyle = PALETTE[colorIndex] || '#ffffff'
      context.fillRect(x, y, count, 1)
      x += count
    })
  })
}

function installExactHeaderLogo(root = document) {
  root.querySelectorAll?.('svg.brand-logo').forEach(svg => {
    const canvas = document.createElement('canvas')
    canvas.className = svg.className.baseVal || 'brand-logo'
    canvas.dataset.paniniExactLogoReady = '1'
    canvas.style.objectFit = 'cover'
    canvas.style.borderRadius = '50%'
    canvas.style.imageRendering = 'auto'
    paintLogo(canvas)
    svg.replaceWith(canvas)
  })
}

